#ifndef LLVM_LIB_TARGET_RISCSIM_RISCSIMSUBTARGET_H
#define LLVM_LIB_TARGET_RISCSIM_RISCSIMSUBTARGET_H

#include "RISCSimFrameLowering.h"
#include "RISCSimISelLowering.h"
#include "RISCSimInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"

#include <string>

#include "llvm/CodeGen/TargetSubtargetInfo.h"

#define GET_SUBTARGETINFO_HEADER
#include "RISCSimGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;

class RISCSimSubtarget : public RISCSimGenSubtargetInfo {

  virtual void anchor();

  RISCSimRegisterInfo RegInfo;
  RISCSimInstrInfo InstrInfo;
  RISCSimFrameLowering FrameLowering;
  RISCSimTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  RISCSimSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
                const TargetMachine &TM);

  // Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const RISCSimInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const RISCSimFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const RISCSimTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const RISCSimRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_RISCSIM_RISCSIMSUBTARGET_H
